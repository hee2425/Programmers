class Solution {
    public int[] solution(int[] arr) {

        //배열 길이가 1인 경우
        if(arr.length == 1){
            int[] answer = {-1};
            return answer;
        }
        
        //가장 작은 수를 뺀 길이로 초기화
        int[] answer = new int[arr.length-1];
        
        int min = arr[0]; 
        for(int i=1; i<arr.length; i++){
            min = Math.min(min, arr[i]);
        }
        
        int index = 0;
        
        for(int i=0; i<arr.length; i++){
            if(arr[i] == min){
                continue;
            }
            answer[index++] = arr[i];
        }
        
        return answer;
    }
}